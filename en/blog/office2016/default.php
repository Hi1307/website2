<h1>Office 2016 vs 2013 - What's The Diff?</h1>
<p>The newest version of Office was recently released, and as you know, Microsoft likes to plan a subtle introduction, introducing it only to its Office 365 subscribers. 
As I managed to grab hold of this product, let me do a review on it.</p>
<h2>Overall</h2>
<p>When you first install it, it has negligable difference with Office 2013. The opening screen closely resembles 2013, although the app starts much faster. 
The interior looks more polished than 2013, and the ribbon now closely resembles the theme colour of the app (blue for Word, red for PowerPoint, etc.). 
It also has a "flatter" feel, adding to the already polished 2013. Another great feature is that it doesn't prompt you to save the item of the item is blank.
